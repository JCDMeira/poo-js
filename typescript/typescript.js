/*
  _ o que é typeScript?

  - é um superset para uma linguagem js
  - que alguns consideram uma linguagem
  - traz algumas funcionalidades novas como por exemplo
    - types
    - features de versões fututas do js
    - interfaces
    - generics
    - decorators
  - nós não executamos ts, compilamos em js e utilizamos a versão gerada
  - utiliza-se em conjunto de outras tecnologias, por ex, react
*/

